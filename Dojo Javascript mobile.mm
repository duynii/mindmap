  <body>
    <p>
      <b>declare.safeMixin </b>primarily accomplishes the same task as lang.mixin, with three important differences:
    </p>
    <ol>
      <li>
        &#160;&#160;&#160;&#160;It can only mix in one object at a time
      </li>
      <li>
        &#160;&#160;&#160;&#160;It will not mix in the constructor property
      </li>
      <li>
        &#160;&#160;&#160;&#160;It will add annotations to functions to make them function properly with declare's this.inherited function
      </li>
    </ol>
    <p>
      
    </p>
    <p>
      Despite what the name implies, there is nothing &#8220;unsafe&#8221; about the original lang.mixin, unless:
    </p>
    <ol>
      <li>
        &#160;&#160;&#160;&#160;You are adding functions to an instance of an object created with declare, and:
      </li>
      <li>
        &#160;&#160;&#160;&#160;&#160;&#160;&#160;&#160;you rely on calls to this.inherited within those new functions, or
      </li>
      <li>
        &#160;&#160;&#160;&#160;&#160;&#160;&#160;&#160;one or more of the mixins contains a constructor property
      </li>
    </ol>
    <p>
      
    </p>
    <p>
      Thus, outside of cases involving instances of instances of declared classes, lang.mixin is likely to be sufficient.
    </p>
    <p>
      
    </p>
    <p>
      declared constructors expose an extend method, which is really just an alias for calling declare.safeMixin with the declared constructor's prototype as the first argument. This is not to be confused with lang.extend, which we will examine next.
    </p>
